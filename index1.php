				<p>  Linux est un système d'exploitation, c'est-à-dire, un ensemble de programmes qui permet d'agir sur la machine et de lancer d'autres programmes.

Un système d'exploitation comprend les programmes fondamentaux dont votre ordinateur a besoin pour échanger des instructions avec les utilisateurs : lire et écrire des données sur disque dur, sur bandes ou vers des imprimantes, contrôler l'utilisation de la mémoire, faire tourner d'autres programmes, etc. La partie la plus importante d'un système d'exploitation est le noyau. Dans un système GNU/Linux, c'est le noyau Linux. Le reste du système comprend d'autres programmes, dont beaucoup ont été écrits par, ou pour, le projet GNU. Comme le noyau Linux seul ne forme pas un système d'exploitation fonctionnel, nous préférons, pour nous référer au système que beaucoup de gens appellent de façon insouciante « Linux », utiliser le terme « GNU/Linux ».

Linux est fondé sur le système d'exploitation Unix. Dès le début, il fut conçu comme un système multitâche et multiutilisateur. Ces caractéristiques suffisent à distinguer Linux d'autres systèmes bien connus. Cependant, Linux est encore plus différent que vous ne pouvez l'imaginer. Personne ne possède Linux, contrairement à d'autres systèmes. L'essentiel de son développement est fait par des volontaires non payés.

Le développement de ce qui fut appelé plus tard GNU/Linux commença en 1984, quand la « Free Software Foundation » entreprit le développement d'un système libre de type Unix, appelé GNU. </p>

